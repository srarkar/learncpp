/*
Compound Data Types
    Compound data types, also known as composite data types, are types defined in terms of other existing data types.
        Every data type is either a fundamental or a compound data type.
    They can have additional properties and behavior that make it easier to solve certain problems.

    C++ supports the following compound types:
        Functions
        C-style Arrays
        Pointer types:
            Pointer to object
            Pointer to function
        Pointer to member types:
            Pointer to data member
            Pointer to member function
        Reference types:
            L-value references
            R-value references
        Enumerated types:
            Unscoped enumerations
            Scoped enumerations
        Class types:
            Structs
            Classes
            Unions
*/